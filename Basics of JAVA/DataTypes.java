public class DataTypes {
    public static void main(String [] args){

        //A variable with datatype boolean
        boolean choice;
        choice = true;
        System.out.println("Value of choice is : " + choice);

        //A variable with datatype char
        char letter;
        letter = '\u0051'; //Unicode character set
        System.out.println("Value of letter is : " + letter);
        
        //A variable with datatype byte
        byte small;
        small = 127;
        System.out.println("Value of small is : " + small);

    }
}